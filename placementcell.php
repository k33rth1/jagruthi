<h2 style="color:darkorange">Placement Objectives</h2>
<p style="font-size:20px">1. Visualize and get requirements, connect to the companies & recruiters through placement portal, visits, build & maintain good relations.<br> 2. Integrate and interface with industry continuously by organizing and co-ordinating frequent guest lectures, industry tours and inplant training and projects of industrial relevance to the students.<br>3. Provide expertise counselling to every student to define their career interest.<br>4. Set up process to establish and strengthen the network with great alumni.<br>5. Organize and co-ordinate campus program to fulfil the commitment of every aspirant.<br>In order to achieve this, the database of all the students of different branches is compiled and furnished to identified industries from time to time. Consequently, as per the manpower requirements of the industries, On or Off Campus or Pool Campus & JOB Fairs Placements are conducted to provide suitable career opportunities to the students.</p>
