<h1 style="color:red">WELCOME</h1>
&nbsp;
<p style="font-size:20px">We have a robust Placement cell that connects by facilitating industry engagement with best companies.JIET Placement Cell understands the current needs of the industry and creates awareness in the students of the various opportunities and openings available for them, well in advance. That is precisely why the College has chosen to invest significant resources in setting up such auxiliary facilities as Phonetics & Interactive Communication Skills Labs with a suitable Qualified Placement Team.The integrated Mentoring, Training & Placement (MTP) Program is a step initiated by the Management of JIET towards achieving this mission.MTP is an innovative and novel program designed to train ALL Students from the very beginning to equip with strong academic knowledge and aggressively exploit opportunities available with Indian and Global Corporates. This program enables every student to become pro-active in defining his own requirements for training so as to enhance her/ his skills.</p>
